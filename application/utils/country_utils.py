# 取号列表集合
AREA_CODES = list(COUNTRY_CODES.values())

# 区号-国家名映射表
CODE_COUNTRY_DICT = {v: k for k, v in COUNTRY_CODES.items()}


def get_country_phone_code(country: str) -> str:
    return COUNTRY_CODES.get(country)


def split_code_phone(phone: str) -> tuple:
    """
    将带有区号的手机号切分成 区号, 手机号
    例:+79771760219
    return:  (7, 9771760219)
    """
    for code in AREA_CODES:
        if phone.startswith(code):
            return code[1:], phone.replace(code, "")
